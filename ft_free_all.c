#include "libft.h"

void	ft_free_all(void **arr)
{
	size_t i;

	if (arr != NULL)
	{
		i = 0;
		while (arr[i] != NULL)
		{
			free(arr[i]);
			i++;
		}
		free(arr);
	}
}
